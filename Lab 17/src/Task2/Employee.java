package Task2;

public class Employee {
    private String name;
    private double salary;
    private double daysAfterPayday;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getSalary() {
        return salary;
    }

    public void setSalary(double salary) {
        this.salary = salary;
    }

    public double getDaysAfterPayday() {
        return daysAfterPayday;
    }

    public void setDaysAfterPayday(double daysAfterPayday) {
        this.daysAfterPayday = daysAfterPayday;
    }
}
